let arr = ["#076585","#00467F","#1488CC","#e65c00","#ff6e7f","#e52d27","#603813","#16A085","#EDE574","#348F50","#1E9600","#000000","#141E30"];
let index = 0;

let btn = document.getElementById("colorCBtn");

btn.addEventListener("click",bgChange);

function bgChange(){
    if(index === arr.length){
        index = 0;
    }
    document.getElementById("wrapper").style.backgroundColor = arr[index];
    index++;

}






































































































































